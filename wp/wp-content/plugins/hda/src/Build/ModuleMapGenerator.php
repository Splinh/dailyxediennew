<?php
/**
 * Module Map Generator
 *
 * Generates a static module map during composer dump-autoload
 * to eliminate runtime reflection and autoloading overhead.
 *
 * Usage: composer dump-autoload (runs automatically via composer.json scripts)
 */

namespace HDAddons\Build;

use Composer\Script\Event;

class ModuleMapGenerator {

    /**
     * Generate static module map
     *
     * Called via composer post-autoload-dump script
     */
    public static function generate(Event $event = null): void {
        $baseDir = dirname(__DIR__, 2);
        $modulesDir = $baseDir . '/src/Modules';
        $outputFile = $baseDir . '/src/Core/module-map.php';

        if (!is_dir($modulesDir)) {
            if ($event) {
                $event->getIO()->write('<error>Modules directory not found</error>');
            }
            return;
        }

        $modules = self::scanModules($modulesDir);

        $content = self::generateMapContent($modules);

        file_put_contents($outputFile, $content);

        if ($event) {
            $event->getIO()->write(sprintf(
                '<info>Generated module map: %d modules found</info>',
                count($modules)
            ));
        }
    }

    /**
     * Scan modules directory for module classes
     */
    private static function scanModules(string $dir): array {
        $modules = [];
        $iterator = new \RecursiveIteratorIterator(
            new \RecursiveDirectoryIterator($dir, \RecursiveDirectoryIterator::SKIP_DOTS)
        );

        foreach ($iterator as $file) {
            if ($file->getExtension() !== 'php') {
                continue;
            }

            $content = file_get_contents($file->getPathname());

            // Extract namespace
            if (!preg_match('/namespace\s+([^;]+);/', $content, $nsMatch)) {
                continue;
            }
            $namespace = trim($nsMatch[1]);

            // Extract class name (must end with Module)
            if (!preg_match('/class\s+(\w+Module)\s+extends\s+AbstractModule/', $content, $classMatch)) {
                continue;
            }
            $className = $classMatch[1];

            // Extract slug from slug() method
            if (!preg_match('/public\s+static\s+function\s+slug\(\)\s*:\s*string\s*\{\s*return\s+[\'"]([^\'"]+)[\'"];/', $content, $slugMatch)) {
                continue;
            }
            $slug = $slugMatch[1];

            // Extract group from group() method
            $group = 'tools'; // default
            if (preg_match('/public\s+static\s+function\s+group\(\)\s*:\s*string\s*\{\s*return\s+[\'"]([^\'"]+)[\'"];/', $content, $groupMatch)) {
                $group = $groupMatch[1];
            }

            // Extract alwaysActive from alwaysActive() method
            $alwaysActive = false;
            if (preg_match('/public\s+static\s+function\s+alwaysActive\(\)\s*:\s*bool\s*\{\s*return\s+(true|false);/', $content, $activeMatch)) {
                $alwaysActive = $activeMatch[1] === 'true';
            }

            $fqcn = $namespace . '\\' . $className;

            $modules[$slug] = [
                'class' => $fqcn,
                'file' => $file->getPathname(),
                'group' => $group,
                'alwaysActive' => $alwaysActive,
            ];
        }

        ksort($modules);

        return $modules;
    }

    /**
     * Generate PHP file content for module map
     */
    private static function generateMapContent(array $modules): string {
        $timestamp = date('Y-m-d H:i:s');
        $count = count($modules);

        $content = <<<PHP
<?php
/**
 * Static Module Map
 *
 * Auto-generated by ModuleMapGenerator
 * DO NOT EDIT MANUALLY
 *
 * Generated: {$timestamp}
 * Modules: {$count}
 */

defined( 'ABSPATH' ) || exit;

return [

PHP;

        foreach ($modules as $slug => $data) {
            $class = $data['class'];
            $group = $data['group'];
            $alwaysActive = $data['alwaysActive'] ? 'true' : 'false';

            $content .= <<<PHP
    '{$slug}' => [
        'class' => {$class}::class,
        'group' => '{$group}',
        'alwaysActive' => {$alwaysActive},
    ],

PHP;
        }

        $content .= "];\n";

        return $content;
    }
}
